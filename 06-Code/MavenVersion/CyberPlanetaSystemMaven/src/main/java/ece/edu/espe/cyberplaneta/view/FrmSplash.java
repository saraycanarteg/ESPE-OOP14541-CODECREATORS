    public void showSplash(int duration) {
        Timer timer = new Timer(duration, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                setVisible(false); 
                dispose();
                
                FrmLogIn login = new FrmLogIn();
                login.setVisible(true);
            }
        });
        timer.setRepeats(false); 
        timer.start(); 

        setVisible(true); 
    }



